    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    String precioP, nombreP, codigoP, descripcionP;
    int  validacion = 0;
    
    codigoP = txt_codigo.getText().trim();
    nombreP = txt_nombre.getText().trim();
    descripcionP = txt_descripcion.getText().trim();
    precioP = txt_precio.getText().trim();
    
    if(codigoP.equals("")){
        txt_codigo.setBackground(Color.red);
        validacion++;
    }
    if(nombreP.equals("")){
        txt_nombre.setBackground(Color.red);
        validacion++;
    }
    if(descripcionP.equals("")){
        txt_descripcion.setBackground(Color.red);
        validacion++;
    }
    if(precioP.equals("")){
        txt_precio.setBackground(Color.red);
        validacion++;
    }
    
        if (validacion == 0) {
            
            precioVerda = Double.parseDouble(precioP);
            
            try {
                
                Connection cn = ConexionLoto.Conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "insert into productos values (?,?,?,?,?)");
               
                pst.setInt(1, 0);
                pst.setString(2, nombreP);
                pst.setString(3, codigoP);
                pst.setString(4, descripcionP);
                pst.setDouble(5, precioVerda);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                JOptionPane.showMessageDialog(null, "El producto se agrego exitosamente!!");
                
                
            } catch (SQLException e) {
                System.err.println("Error al registrar el producto! " + e);
                JOptionPane.showMessageDialog(null, "Error al registrar el producto!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        }
    
    
    }//GEN-LAST:event_jButton1ActionPerformed

