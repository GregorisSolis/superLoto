    private void jButton_registrarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrarFuncionarioActionPerformed

         String nombreF, nombreUser, passwordF, cargo;
         int validar = 0;
         
         nombreF = txt_nombreFuncionario.getText().trim();
         nombreUser = txt_nombreUser.getText().trim();
         passwordF = txt_passFuncionario.getText().trim();
         cargo = cmb_cargo.getSelectedItem().toString();
         
         if(nombreF.equals("")){
             txt_nombreFuncionario.setBackground(Color.red);
             validar++;
         }
         if(nombreUser.equals("")){
             txt_nombreUser.setBackground(Color.red);
             validar++;
         }
         if(passwordF.equals("")){
             txt_passFuncionario.setBackground(Color.red);
             validar++;
         }
         
         if(validar == 0){
             
             try {
                 
                 Connection cn = ConexionLoto.Conectar();
                 PreparedStatement pst = cn.prepareStatement(
                         "insert into funcionarios values (?,?,?,?,?)");
                 
                 pst.setInt(1, 0);
                 pst.setString(2, nombreF);
                 pst.setString(3, nombreUser);
                 pst.setString(4, passwordF);
                 pst.setString(5, cargo);
                 
                 pst.executeUpdate();
                 cn.close();
                 
                 Limpiar();
                 
                 JOptionPane.showMessageDialog(null, "Funcionario registrado exitosamente.");
                 
             } catch (SQLException e) {
                 System.err.println("Error al registrar funcionario. " + e );
                 JOptionPane.showMessageDialog(null, "Error al registrar funcionario.");
             }
         }else{
             JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
         }
    }//GEN-LAST:event_jButton_registrarFuncionarioActionPerformed

