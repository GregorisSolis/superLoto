    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
          
        int validacion = 0;
        String nombreP, codigoP, precioP, descripcionP;
        
        nombreP = txt_nombre.getText().trim();
        codigoP = txt_codigo.getText().trim();
        descripcionP = txt_descripcion.getText().trim();
        precioP = txt_precio.getText().trim();
        
        if(nombreP.equals("")){
            txt_nombre.setBackground(Color.red);
            validacion++;
        }
        if(codigoP.equals("")){
            txt_codigo.setBackground(Color.red);
            validacion++;
        }
        if(descripcionP.equals("")){
            txt_descripcion.setBackground(Color.red);
            validacion++;
        }
        if(precioP.equals("")){
           txt_precio.setBackground(Color.red);
            validacion++;
        }
        
        if(validacion == 0){
            
            try {
                
                Connection cn = ConexionLoto.Conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "update productos set nombre_producto=?, codigo_barra=?, descripcion=?, precio=?"
                + "where codigo_barra = '" + IDcodigo_update + "'");
                
                pst.setString(1, nombreP);
                pst.setString(2, codigoP);
                pst.setString(3, descripcionP);
                pst.setString(4, precioP);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                txt_nombre.setBackground(Color.green);
                txt_codigo.setBackground(Color.green);
                txt_descripcion.setBackground(Color.green);
                txt_precio.setBackground(Color.green);
                
                JOptionPane.showMessageDialog(null, "Producto registrado exitosamente");
                this.dispose();
                
            } catch (SQLException e) {
                System.err.println("Error en actualiar producto " + e);
                JOptionPane.showMessageDialog(null, "Error al actualizar el producto");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        }
        
        
    }//GEN-LAST:event_jButton_actualizarActionPerformed

