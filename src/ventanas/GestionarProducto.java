    private void jButton_buscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscarProductoActionPerformed
         
        IDcodigo_update = txt_codigo.getText().trim();
        DefaultTableModel model = new DefaultTableModel();
        
        try {
            
            Connection cn = ConexionLoto.Conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select id_productos, nombre_producto, codigo_barra, descripcion, precio from productos where codigo_barra = '"
                    + IDcodigo_update + "'" );
                     
            
            ResultSet rs = pst.executeQuery();
            
           
        jTable_listaBuscador = new JTable(model);
        jScrollPane1.setViewportView(jTable_listaBuscador);
        
        model.addColumn("ID");
        model.addColumn("Nombre");
        model.addColumn("Descripcion");
        model.addColumn("Codigo");
        model.addColumn("Precio");
        
        if(rs.next()){
            Object[] fila = new Object[5];
            for( int i = 0; i < 5; i++){
                fila[i] = rs.getObject(i + 1);
            }
            model.addRow(fila);
        }
        else{
            JOptionPane.showMessageDialog(null, "Producto no registrado!");
        }
        cn.close();
            
        } catch (SQLException e) {
            System.err.println("no se pudo conectar ni crear la lista... " + e);
            JOptionPane.showMessageDialog(null, "No se pudo conectar a la base de datos");
        }
        
        jTable_listaBuscador.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = jTable_listaBuscador.rowAtPoint(e.getPoint());
                int columna_point = 2;
                
                if(fila_point > -1){
                    IDcodigo_update = (String)model.getValueAt(fila_point, columna_point);
                    
                    EditarProducto editarProducto = new EditarProducto();
                    editarProducto.setVisible(true);
                }
            }
        });
                
    }//GEN-LAST:event_jButton_buscarProductoActionPerformed

